package com.example.eventhubapp;

public class Facilities {
    private String checkParking;
    private String checkAc;
    private String checkWifi;
    private String checkDeco;

    public Facilities() {
    }

    public String getCheckParking() {
        return checkParking;
    }

    public void setCheckParking(String checkParking) {
        this.checkParking = checkParking;
    }

    public String getCheckAc() {
        return checkAc;
    }

    public void setCheckAc(String checkAc) {
        this.checkAc = checkAc;
    }

    public String getCheckWifi() {
        return checkWifi;
    }

    public void setCheckWifi(String checkWifi) {
        this.checkWifi = checkWifi;
    }

    public String getCheckDeco() {
        return checkDeco;
    }

    public void setCheckDeco(String checkDeco) {
        this.checkDeco = checkDeco;
    }
}
